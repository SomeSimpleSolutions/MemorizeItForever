import UIKit

extension UIColor{
    convenience init(red: Float, green: Float, blue: Float){
        self.init(red: CGFloat(red) / 255.0, green: CGFloat(green) / 255.0, blue: CGFloat(blue) / 255.0, alpha: CGFloat(1))
    }
}